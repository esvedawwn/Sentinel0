/**
 * Reads the Drizzle-generated SQL migration and emits a TypeScript source file
 * that exports the statements as a string array.  This means esbuild bundles
 * the SQL inline — no external files are needed in the pkg binary at runtime.
 *
 * Usage (run from repo root):
 *   node lib/db/scripts/codegen-migrations.mjs
 *
 * Regenerate after schema changes:
 *   pnpm --filter @workspace/db exec drizzle-kit generate
 *   node lib/db/scripts/codegen-migrations.mjs
 */

import { readFileSync, readdirSync, writeFileSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const DRIZZLE_DIR = join(__dirname, "..", "drizzle");
const OUT_FILE    = join(__dirname, "..", "src", "migrations-snapshot.ts");

// Collect all .sql files in drizzle/ sorted by name (numeric order).
const sqlFiles = readdirSync(DRIZZLE_DIR)
  .filter((f) => f.endsWith(".sql"))
  .sort();

if (sqlFiles.length === 0) {
  console.error(
    "No .sql files found in lib/db/drizzle/.\n" +
    "Run: pnpm --filter @workspace/db exec drizzle-kit generate"
  );
  process.exit(1);
}

const allStatements = [];

for (const file of sqlFiles) {
  const sql = readFileSync(join(DRIZZLE_DIR, file), "utf8");
  const stmts = sql
    .split("--> statement-breakpoint")
    .map((s) => s.trim())
    .filter(Boolean);
  allStatements.push(...stmts);
}

const content =
  `// AUTO-GENERATED by lib/db/scripts/codegen-migrations.mjs — do not edit manually.\n` +
  `// Re-generate after schema changes:\n` +
  `//   pnpm --filter @workspace/db exec drizzle-kit generate\n` +
  `//   node lib/db/scripts/codegen-migrations.mjs\n` +
  `//\n` +
  `// Source SQL files: ${sqlFiles.join(", ")}\n` +
  `export const migrationStatements: readonly string[] = ` +
  JSON.stringify(allStatements, null, 2) +
  `;\n`;

writeFileSync(OUT_FILE, content, "utf8");
console.log(
  `Generated lib/db/src/migrations-snapshot.ts (${allStatements.length} statements from ${sqlFiles.length} SQL file(s))`
);
